#include <stdlib.h>

void			ft_filltab(char tab[21][61])
{
	int			i;
	int			j;

	i = 0;
	j = 0;
	while (i < 20)
	{
		if (i == 0 || i == 19)
		{
			while (j < 60)
			{
				tab[i][j] = '#';
				++j;
			}
		}
		else
		{
			while (j < 60)
			{
				if (j == 0 || j == 59)
					tab[i][j] = '#';
				else
					tab[i][j] = ' ';
				++j;
			}
		}
		tab[i][j] = '\0';
		j = 0;
		++i;
	}
	tab[20][0] = '\0';
}
