void			ft_filltab(char tab[60][20])
{
	int			i;

	i = 0;
	while (tab)
	{
		j = 0;
		if (i == 0 || i == 59)
		{
			while (tab[i][j])
				tab[i][j] = '*';
			j = 0;
		}
		while (tab[i][j])
		{
			if (j = 0 || j = 19)
				tab[i][j] = '*';
			tab[i][j] = ' ';
			++j;
		}
		++i;
	}
}
